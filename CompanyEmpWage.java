public class CompanyEmpWage {
    private String companyName;
    private int wagePerHour;
    private int totalWorkingHour;
    private int totalWorkingDaysInAMonth;

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public int getWagePerHour() {
        return wagePerHour;
    }

    public void setWagePerHour(int wagePerHour) {
        this.wagePerHour = wagePerHour;
    }

    public int getTotalWorkingHour() {
        return totalWorkingHour;
    }

    public void setTotalWorkingHour(int totalWorkingHour) {
        this.totalWorkingHour = totalWorkingHour;
    }

    public int getTotalWorkingDaysInAMonth() {
        return totalWorkingDaysInAMonth;
    }

    public void setTotalWorkingDaysInAMonth(int totalWorkingDaysInAMonth) {
        this.totalWorkingDaysInAMonth = totalWorkingDaysInAMonth;
    }
}
